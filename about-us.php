            <script>
                // Simple slider logic for mobile (show one card at a time)
                document.addEventListener('DOMContentLoaded', function() {
                    const cards = document.querySelectorAll('.vision-card');
                    const dotsContainer = document.querySelector('.vision-slider-dots');
                    if (window.innerWidth < 768 && cards.length > 1) {
                        let current = 0;
                        cards.forEach((c, i) => c.style.display = i === 0 ? 'block' : 'none');
                        // Create dots
                        for (let i = 0; i < cards.length; i++) {
                            const dot = document.createElement('div');
                            dot.className = 'vision-slider-dot' + (i === 0 ? ' active' : '');
                            dot.addEventListener('click', () => {
                                cards[current].style.display = 'none';
                                dotsContainer.children[current].classList.remove('active');
                                current = i;
                                cards[current].style.display = 'block';
                                dotsContainer.children[current].classList.add('active');
                            });
                            dotsContainer.appendChild(dot);
                        }
                    }
                });
